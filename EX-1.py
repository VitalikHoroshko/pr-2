a = float(input("Введіть перше число: "))
b = float(input("Введіть друге число: "))
c = float(input("Введіть третє число: "))

if a >= 0:
  a = a ** 2
else:
  a = a ** 4
if b >= 0:
  b = b ** 2
else:
  b = b ** 4
if c >= 0:
  c = c ** 2
else:
  c = c ** 4
print("Результат для першого числа:", a)
print("Результат для другого числа:", b)
print("Результат для третього числа:", c)
